internal enum WinFormsTarget { Html, Maui, Unity }

internal class WinFormsControl
{
    public string Type     { get; set; } = string.Empty;
    public string Name     { get; set; } = string.Empty;
    public string Text     { get; set; } = string.Empty;
    public (int X, int Y)         Location = (0, 0);
    public (int Width, int Height) Size    = (100, 30);
    public string ForeColor   { get; set; } = string.Empty;
    public string BackColor   { get; set; } = string.Empty;
    public string FontFamily  { get; set; } = "Segoe UI";
    public float  FontSize    { get; set; } = 9;
    public bool   FontBold    { get; set; }
    public bool   FontItalic  { get; set; }
    public bool   Checked     { get; set; }
    public bool   Enabled     { get; set; } = true;
    public bool   Visible     { get; set; } = true;
    public double Value       { get; set; }
    public double Minimum     { get; set; }
    public double Maximum     { get; set; } = 100;
    public bool   MultiSelect { get; set; }
    public string ImagePath   { get; set; } = string.Empty;
    public List<string>              Items  { get; } = new();
    public Dictionary<string, string> Events { get; } = new(StringComparer.OrdinalIgnoreCase);
}

/// <summary>Pascal-case helper used across converters.</summary>
internal static class NameExtensions
{
    public static string ToPascalCase(this string s)
    {
        if (string.IsNullOrEmpty(s)) return "Element";
        return char.ToUpperInvariant(s[0]) + s[1..];
    }
}
